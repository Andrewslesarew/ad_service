package com.perspective.vpng.actor.common_import.exception;

public class DataSourceReadingException extends Exception {

    public DataSourceReadingException() {
        super();
    }

    public DataSourceReadingException(String message) {
        super(message);
    }

    public DataSourceReadingException(String message, Throwable cause) {
        super(message, cause);
    }

    public DataSourceReadingException(Throwable cause) {
        super(cause);
    }

}
