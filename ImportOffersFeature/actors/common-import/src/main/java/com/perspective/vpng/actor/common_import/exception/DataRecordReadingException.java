package com.perspective.vpng.actor.common_import.exception;

public class DataRecordReadingException extends Exception {

    public DataRecordReadingException() {
    }

    public DataRecordReadingException(String message) {
        super(message);
    }

    public DataRecordReadingException(String message, Throwable cause) {
        super(message, cause);
    }

    public DataRecordReadingException(Throwable cause) {
        super(cause);
    }

}
